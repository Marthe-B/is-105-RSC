package sum

// SumInt8
func SumInt8(a, b int8) int8 {
	return a + b
}

// SumUint32
func SumUint32(a, b uint32) uint32 {
	return a + b
}

// SumInt32
func SumInt32(a, b int32) int32 {
	return a + b
}

// SumFloat64
func SumFloat64(a, b float64) float64 {
	c := a + b
	return c
}

// SumInt64
func SumInt64(a, b int64) int64 {
	return a + b
}
